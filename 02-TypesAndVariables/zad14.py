# Calculation of circle area and circumference
###
# determine radius and pi
radius = 5
pi = 3.14
# calculate area
area = float(pi) * int(radius) ** 2
# calculate circumference
circumference = 2 * float(pi) * int(radius)
# display results
print(f'Pole koła wynosi: {area}\n Obwód koła wynosi: {round(circumference, 2)}')

